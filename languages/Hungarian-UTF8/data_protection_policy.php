<h3>Adatvédelem</h3>
<p>A webhely tulajdonosai nagyon komolyan veszik az Ön személyes adatainak védelmét. Személyes adatait bizalmasan kezeljük és
a törvényi adatvédelmi előírásoknak és ezen adatvédelmi irányelveknek megfelelően.</p>

<p>Weboldalunk használata személyes adatok megadása nélkül lehetséges. Ha személyes adatokat kérnek (például név és e-mail cím a kapcsolatfelvételi űrlapon), ezt önként adják meg. Ezeket az adatokat nem továbbítjuk harmadik feleknek.</p>

<p>Felhívjuk a figyelmet arra, hogy az információk interneten keresztüli küldése biztonsági kérdések tárgyát képezi.
Az adatok teljes védelme harmadik fél általi hozzáférés ellen nem lehetséges.</p>

<h3>Cookies</h3>
<p>Ez a weboldal sütiket használ. A sütik nem károsítják a számítógépet, és nem tartalmaznak vírusokat. A sütik fontos funkciókat töltenek be a webhely működésében. A sütik kis szöveges fájlok, amelyeket a böngésző a számítógépén tárol.</p>

<p>Az általunk használt sütik többsége "session cookie". A böngésző bezárásakor automatikusan törlődnek. Egyéb sütik
addig maradnak a készüléken, amíg nem törli őket. Ezek a cookie-k lehetővé teszik számunkra a böngésző felismerését, amikor legközelebb meglátogatja, és visszaállítja néhány preferenciáját (például az Ön által preferált nyelvet).</p>

<p>Konfigurálhatja a böngésző beállításait úgy, hogy tájékozott legyen a sütik beállításáról, és csak bizonyos esetekben engedélyezze a sütik használatát.
A sütik letiltása korlátozhatja a weboldal működését.</p>

<h3>Szerver naplófájlok</h3>
<p>Ezen oldalak szolgáltatója automatikusan információkat gyűjt a szerver naplófájljaiban, amelyeket az Ön böngészője továbbít nekünk. Ezek:</p>

	<ul>
		<li>A böngésző típusa és verziója</li>
		<li>Operációs rendszer</li>
		<li>URL cím</li>
		<li>A hozzáférő számítógép gazdagép neve</li>
		<li>IP address (anonimizált)</li>
		<li>Kérés időpontja</li>
	</ul>
<p>Ezeket az adatokat nem lehet hozzárendelni meghatározott személyekhez. Ezeket az adatokat nem egyesítjük más adatforrásokkal. Fenntartjuk a jogot, hogy visszamenőleg ellenőrizzük ezeket az adatokat, ha tudomást szerezünk az illegális felhasználásra vonatkozó konkrét jelekről.</p>

<h3>Kapcsolatfelvételi űrlap</h3>
<p>Ha a kapcsolatfelvételi űrlapon keresztül küld nekünk kérdéseket, a kérdőív adatai, beleértve az ott megadott elérhetőségeket is,
tárolásra kerül a kérés feldolgozása és utólagos kérdések esetén. Természetesen nem adjuk át ezeket az adatokat.</p>

